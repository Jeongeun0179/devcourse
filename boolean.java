public class DataTypeEx01 {

    public static void main (String[] args) {
        //프로그램 시작

        //논리형 데이터(Boolean) - true / false
        boolean bool1;
        bool1 = true;
        System.out.println( bool1 );
        
        // true는 무조건 소문자로
        boolean bool2;
        bool2 = True;
        System.out.println( bool2 );

        //프로그램 끝
    }
}
