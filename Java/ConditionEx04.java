public class ConditionEx04 {
    public static void main(String[] args) {
        //data 변수값이 10이면 "10이다" 아니면 "10이 아니다" 출력
        System.out.println("시작");

        int data = 10;
        if (data == 10) { 
            System.out.println("10이다");
        } else {
            System.out.println("10이 아니다");
        }
        System.out.println("끝");
    }
    
}
