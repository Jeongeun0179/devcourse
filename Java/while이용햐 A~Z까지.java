public class LoopEx11 {
    public static void main(String[] args) {
        // A ~ Z
        // while
        System.out.println( "시작" );

        int i = 65;
        while ( i<=90 ) {
            System.out.println((char)i);
            i++;
        }


        System.out.println( "끝" );

    }
    
}
